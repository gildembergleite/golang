package main

import (
	"fmt"
)

func main() {
    var numero int      // Declaração de uma variável 'numero' do tipo int
    var nome string     // Declaração de uma variável 'nome' do tipo string
    var ativo bool      // Declaração de uma variável 'ativo' do tipo bool

    fmt.Println("Valor de 'numero':", numero) // Saída: Valor de 'numero': 0
    fmt.Println("Valor de 'nome':", nome)     // Saída: Valor de 'nome':
    fmt.Println("Valor de 'ativo':", ativo)   // Saída: Valor de 'ativo': false
}
